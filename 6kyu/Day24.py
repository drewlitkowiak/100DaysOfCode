def is_language_diverse(lst): 
    # Count occurences of allowed languages
    language_counts = {
        "Python": 0,
        "Ruby": 0,
        "JavaScript": 0
    }
    for language in (dev['language'] for dev in lst):
        if language in language_counts:
            language_counts[language] += 1
    # Check if the max count is at most twice the min count
    counts = language_counts.values()
    return max(counts) <= min(counts) * 2

